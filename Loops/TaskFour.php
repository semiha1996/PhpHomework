//rows - редове , cols - колони

//Определя се броят редове (5)
for($rows = 0; $rows < 5; $rows++){
    //Определя се броя на колоните според условието $cols<=$rows.
   for($cols = 0; $cols <= $rows; $cols++) { 
       echo '*';//Принтира символите
   }
   echo '</br>';
}